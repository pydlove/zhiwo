def extract_theme_colors(doc):
    """提取模板主题色"""
    colors = set()
    for p in doc.paragraphs:
        for run in p.runs:
            if run.font.color and run.font.color.rgb:
                colors.add(str(run.font.color.rgb))
    return list(colors)

def is_title_para(p, text):
    """判断是否为标题段落"""
    return p.style.name == 'Heading 1' or (len(text) <= 15 and any(kw in text for kw in ['生活', '心安', '效率', '温柔', '独处', '甜蜜', '科技', 'AI']))

def is_subtitle_para(p, text):
    """判断是否为副标题"""
    return len(text) <= 25 and any(kw in text for kw in ['不是', '而是', '当', '在', '人生', '拥有的', '你有没有'])

def is_caption_para(p, text):
    """判断是否为图片说明"""
    return '▲' in text or '▼' in text

def is_separator_para(p, text):
    """判断是否为分隔符"""
    return all(c in text for c in ['—', '◆']) or '· · ·' in text or '━━━━' in text or '▬▬▬' in text

def is_section_title_para(p, text):
    """判断是否为小节标题"""
    return bool(re.match(r'^(\d{2}\s*/|01\s*/|02\s*/|03\s*/|04\s*/|✦|⚠️|💡)', text))

def is_quote_para(p, text):
    """判断是否为引用/金句"""
    return any(kw in text for kw in ['✿', '✦', '· 每一件', '— 过去一年', '研究表明', '日本', '愿你'])

def set_para_text_keep_style(para, new_text):
    """替换段落文字但保留第一个run的样式"""
    if not para.runs:
        para.text = new_text
        return

    first_run = para.runs[0]
    # 保存格式
    font_name = first_run.font.name
    font_size = first_run.font.size
    font_bold = first_run.font.bold
    font_color = first_run.font.color.rgb if first_run.font.color and first_run.font.color.rgb else None

    # 清空所有runs
    for run in para.runs:
        run.text = ''

    # 设置新文字到第一个run
    first_run.text = new_text

    # 恢复格式
    if font_name:
        first_run.font.name = font_name
    if font_size:
        first_run.font.size = font_size
    if font_bold is not None:
        first_run.font.bold = font_bold
    if font_color:
        first_run.font.color.rgb = font_color


