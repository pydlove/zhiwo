def download_image(keyword, output_path, width=800, height=450):
    """下载随机图片作为配图"""
    try:
        # 使用 picsum.photos 获取随机图片
        url = f"https://picsum.photos/{width}/{height}?random={hash(keyword) % 10000}"
        resp = requests.get(url, timeout=30)
        if resp.status_code == 200:
            with open(output_path, 'wb') as f:
                f.write(resp.content)
            return output_path
    except Exception as e:
        print(f"下载图片失败: {e}")
    return None

