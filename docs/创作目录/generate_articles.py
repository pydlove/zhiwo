def set_paragraph_text(para, text, color=None, size=None, bold=None, font_name=None):
    """设置段落文字，应用指定的格式"""
    if not para.runs:
        run = para.add_run(text)
    else:
        run = para.runs[0]
        run.text = text
        for r in para.runs[1:]:
            r.text = ''
    if color:
        run.font.color.rgb = RGBColor(int(color[0:2], 16), int(color[2:4], 16), int(color[4:6], 16))
    if size:
        run.font.size = Pt(size)
    if bold is not None:
        run.font.bold = bold
    if font_name:
        run.font.name = font_name

