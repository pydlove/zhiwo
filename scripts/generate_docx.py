def create_docx(article, filepath):
    doc = Document()

    # 标题
    title = doc.add_heading(article["title"], level=1)
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    for run in title.runs:
        run.font.size = Pt(18)
        run.font.bold = True
        run.font.color.rgb = RGBColor(0, 0, 0)

    # 引言
    intro = doc.add_paragraph()
    intro.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    intro_run = intro.add_run("【导语】")
    intro_run.bold = True
    intro_run.font.size = Pt(12)
    intro_run.font.color.rgb = RGBColor(0, 112, 192)
    intro.add_run("\n")
    intro_text = doc.add_paragraph(article["sections"][0][1])
    intro_text.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    intro_text.runs[0].font.size = Pt(11)

    # 正文
    for i, (section_title, section_content) in enumerate(article["sections"]):
        if i == 0:
            continue  # 引言已用第一段内容
        heading = doc.add_heading(section_title, level=2)
        for run in heading.runs:
            run.font.size = Pt(14)
            run.font.bold = True
            run.font.color.rgb = RGBColor(0, 0, 0)

        p = doc.add_paragraph(section_content)
        p.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
        p.paragraph_format.line_spacing = 1.5
        for run in p.runs:
            run.font.size = Pt(11)

    # 结语
    doc.add_paragraph()
    conclusion_heading = doc.add_heading("结语", level=2)
    for run in conclusion_heading.runs:
        run.font.size = Pt(14)
        run.font.bold = True

    conclusion_p = doc.add_paragraph(article["conclusion"])
    conclusion_p.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    conclusion_p.paragraph_format.line_spacing = 1.5
    for run in conclusion_p.runs:
        run.font.size = Pt(11)
        run.font.italic = True

    doc.save(filepath)
    print(f"已生成: {filepath}")


for idx, article in enumerate(articles, 1):
    filepath = os.path.join(output_dir, f"公众号创作技巧_{idx:02d}_{article['title']}.docx")
    create_docx(article, filepath)

